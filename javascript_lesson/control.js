//JavaScriptの制御
if(1){
  console.log("1はtrueです")
}

if(0){
  console.log("0はtrueです")
}else{
  console.log("0はfalseです")
}

if(null){
  console.log("nullはtrueです")
}else{
  console.log("nullはfalseです")
}

if(undefined){
  console.log("undefinedはtrueです")
}else{
  console.log("undefinedはfalseです")
}

if(""){
  console.log('""はtrueです')
}else{
  console.log('""はfalseです')
}

if("hello"){
  console.log('"hello"はtrueです')
}else{
  console.log('"hello"はfalseです')
}

if([]){
  console.log('[]はtrueです')
}else{
  console.log('[]はfalseです')
}

if([].length){
  console.log('[].lengthはtrueです')
}else{
  console.log('[].lengthはfalseです')
}

if([1].length){
  console.log('[1].lengthはtrueです')
}else{
  console.log('[1].lengthはfalseです')
}

if({name:"hello"}){
  console.log('{}はtrueです');
}else{
  console.log('{}はfalseです');
}


